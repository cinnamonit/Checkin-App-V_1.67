using System;
namespace Checkin
{
	public class RemarkDetails
	{
		private string mainRemark;

		private string subRemark;

		public string MainRemark
		{
			get
			{
				return mainRemark;
			}

			set
			{
				mainRemark = value;
			}
		}

		public string SubRemark
		{
			get
			{
				return subRemark;
			}

			set
			{
				subRemark = value;
			}
		}
	}
}

